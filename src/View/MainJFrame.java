        jPanel8.setBackground(Color.decode("#2b313b"));
        ImageIcon img = new ImageIcon("decor.png");

        Image b_img = img.getImage();
        Image temp_img = b_img.getScaledInstance(800, 35, Image.SCALE_SMOOTH);
