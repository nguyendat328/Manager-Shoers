    public void resetColor(JPanel[] pane, JLabel[] label) {
        for (int i = 0; i < pane.length; i++) {
            pane[i].setBackground(Color.decode(listTheme.get(0).getBackgroundMenuBar()));

        }
        for (int i = 0; i < label.length; i++) {
            label[i].setForeground(Color.decode(listTheme.get(0).getTxtColorTitle()));
        }

    }
