        discount = OCC.getDiscout();
        // set option
        if (setting != null || setting.size() > 0) {
            nameTheme = setting.get(0).getThemeName();
            if (nameTheme.equalsIgnoreCase("Ocean") || nameTheme.equalsIgnoreCase("Colorfull")) {
                url = "../View/images/circle/";
            }
            sellingMethod = setting.get(0).getSellingMethod();
            listTheme = ThemeC.getTheme(nameTheme);
        } else {
            listTheme = ThemeC.getTheme("dark");
        }
        // set discount
        if (discount != null || discount.size() > 0) {
            LocalDate date = LocalDate.now();
            int diss = discount.get(0).getDiscount();
            LocalDate disFromDate = discount.get(0).getFromDate();
            LocalDate disToDate = discount.get(0).getToDate();
            if (disFromDate.isAfter(date) || disToDate.isBefore(date) || diss <= 0) {
            } else {
                dis = discount.get(0).getDiscount();
                discountFromDate = discount.get(0).getFromDate();
                discountToDate = discount.get(0).getToDate();
            }
        } else {
            dis = 0;
        }

        userLabel.setText("Xin chào: " + userName);
        // GM.setLabelIcon(jLabel7, "images/order_create.png");
        Theme theme = MainJFrame.listTheme.get(0);
        taskPanel.setBackground(Color.decode(theme.getBackgroundMainPanel()));
        setColor(btnBH, bhLabel);
        resetColor(new JPanel[]{btnFile, btnNhap, btnBC}, new JLabel[]{fileLabel, nhapLabel, bcLabel});
        GM.showPanel(taskPanel, new BHPanel(this));
